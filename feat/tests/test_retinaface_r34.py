@pytest.mark.skipif(not _have_weights(), reason="local R34 safetensors not present")
def test_wrapper_batched_matches_serial():
    """Batched call produces per-image outputs equivalent to serial 1-image
    calls. Keeps us honest about the on-device batched-NMS contract."""
    rf = _make_wrapper()
    paths = [
        os.path.join(get_test_data_path(), "single_face.jpg"),
        os.path.join(get_test_data_path(), "multi_face.jpg"),
    ]
    for p in paths:
        if not os.path.exists(p):
            pytest.skip(f"{p} missing")

    from feat.data import ImageDataset
    from torch.utils.data import DataLoader

    ds = ImageDataset(paths, output_size=640, preserve_aspect_ratio=True, padding=True)
    loader = DataLoader(ds, batch_size=2)
    batch = next(iter(loader))["Image"]

    batched_out = rf(batch)
    serial_out = [rf(batch[i : i + 1])[0] for i in range(batch.shape[0])]

    assert len(batched_out) == len(serial_out) == 2
    for img_idx in range(2):
        assert len(batched_out[img_idx]) == len(serial_out[img_idx]), (
            f"image {img_idx}: batched got {len(batched_out[img_idx])} "
            f"faces, serial got {len(serial_out[img_idx])}"
        )


# -------------------- Detectorv1 integration -------------------------------


@pytest.mark.skipif(not _have_weights(), reason="local R34 safetensors not present")
def test_detector_with_retinaface_detects_multi_face():
    """End-to-end: Detectorv1(face_model='retinaface') -> detect() -> Fex.
    Verifies the dispatch in detect_faces, the per-frame extraction path,
    and that pose columns end up NaN."""
    # Stub the HF download to point at the local safetensors so the test
    # doesn't require the file to be uploaded yet. The Retinaface wrapper
    # does a local import inside __init__, so we have to patch the source
    # symbol on feat.utils, not the importing module.
    import feat.utils as _utils

    orig_dl = _utils.hf_hub_download_with_fallback

    def _local_download(repo_id, filename, fallback_filename, cache_dir):
        if "retinaface_r34" in repo_id:
            return str(_LOCAL_WEIGHTS)
        return orig_dl(repo_id, filename, fallback_filename, cache_dir)

    _utils.hf_hub_download_with_fallback = _local_download
    try:
        from feat.detector import Detectorv1

        det = Detectorv1(face_model="retinaface", device="cpu", au_model="svm")
        assert det.info["face_model"] == "retinaface"
        # facepose_model = 'pose_mlp' indicates pose came from the Pose-MLP
        # (distilled from img2pose) rather than img2pose's native regression.
        # The legacy DLT-PnP backend was removed; retinaface always uses the MLP.
        assert det.info["facepose_model"] == "pose_mlp"

        path = os.path.join(get_test_data_path(), "multi_face.jpg")
        if not os.path.exists(path):
            pytest.skip("multi_face.jpg missing")

        fex = det.detect(path)
        assert len(fex) >= 4, f"expected >=4 faces, got {len(fex)}"
        # Pose columns are populated via the Pose-MLP. Each angle must be a
        # finite number in radians (between -pi and +pi).
        import math
        for col in ("Pitch", "Roll", "Yaw"):
            vals = fex[col]
            assert not vals.isna().any(), f"{col} has NaN values"
            assert (vals.abs() <= math.pi).all(), f"{col} out of [-pi, pi]"
        # Sanity bound: faces in multi_face.jpg are all roughly frontal
        # (pictured group facing the camera), so mean Yaw magnitude across
        # the 5 detected faces should be modest. Loose enough to pass on
        # any reasonable PnP solution; tight enough to catch a regression
        # where the solver returns near-pi values for every face.
        assert fex.Yaw.abs().mean() < math.radians(45), (
            f"mean |Yaw| = {math.degrees(fex.Yaw.abs().mean()):.1f} deg "
            "on a roughly-frontal multi-face group is suspicious"
        )
        # Face / emotion outputs are real numbers.
        assert not fex.FaceRectX.isna().any()
        assert (fex.happiness > 0).any()
    finally:
        _utils.hf_hub_download_with_fallback = orig_dl
