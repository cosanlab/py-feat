"""Tests for the RetinaFace-R34 port + Detectorv1 integration.

Pins three layers of contract:

1. **Model**: ``RetinaFace`` constructs cleanly and produces the right
   tensor shapes for a batched input.
2. **Wrapper**: ``Retinaface`` (the ``__call__`` interface used by Detectorv1
   and pretrained.py) does its postprocess on-device, accepts batches,
   and produces equivalent per-image output for both batched and serial
   calls.
3. **Detectorv1 integration**: ``Detectorv1(face_model='retinaface')``
   builds, detects faces in a real test image, and propagates NaN-filled
   pose columns (since R34 has no head-pose regression).

Most tests skip if the local-fixture safetensors file is missing; the
file is generated by ``scripts/prepare_retinaface_r34.py``. CI should
populate it from the HF download once the file is uploaded.
"""

from __future__ import annotations

import os
from pathlib import Path

import pytest
import torch

from feat.face_detectors.Retinaface.Retinaface_model import RetinaFace, RETINAFACE_R34_CFG
from feat.face_detectors.Retinaface.Retinaface_test import Retinaface
from feat.utils.io import get_test_data_path


# Local safetensors file produced by scripts/prepare_retinaface_r34.py.
# The PR description includes an upload step; once the file is on HF the
# Retinaface wrapper will fetch it automatically. For local dev, point at
# the temp file or skip.
_LOCAL_WEIGHTS = Path("/tmp/py-feat-v2-models/retinaface_r34.safetensors")


def _have_weights() -> bool:
    return _LOCAL_WEIGHTS.exists()


def _load_local_weights(model: RetinaFace) -> RetinaFace:
    from safetensors.torch import load_file

    state_dict = load_file(str(_LOCAL_WEIGHTS))
    model.load_state_dict(state_dict, strict=True)
    return model


def _make_wrapper(device="cpu"):
    """Build the Retinaface wrapper without HF download, weights loaded locally."""
    rf = Retinaface(device=device, pretrained=None)
    _load_local_weights(rf.net)
    rf.net.to(rf.device).eval()
    return rf


# -------------------- model-level (no weights needed) --------------------


def test_retinaface_model_constructs_with_default_cfg():
    """Builds without errors; backbone is torchvision resnet34."""
    model = RetinaFace()
    assert model.cfg["name"] == "resnet34"


def test_retinaface_model_forward_shapes():
    """Forward on a dummy [1, 3, 320, 320] input returns the right shapes."""
    model = RetinaFace().eval()
    x = torch.zeros(1, 3, 320, 320)
    with torch.no_grad():
        bbox, conf, ldm = model(x)
    # 320x320 with strides 8/16/32 + 2 anchors per location:
    # 40x40*2 + 20x20*2 + 10x10*2 = 4200
    assert bbox.shape == (1, 4200, 4)
    assert conf.shape == (1, 4200, 2)
    assert ldm.shape == (1, 4200, 10)


def test_retinaface_model_rejects_non_resnet34_cfg():
    """Only the ResNet34 backbone is shipped here. Other configs error
    explicitly so a user passing a stale legacy cfg fails loudly."""
    bad_cfg = dict(RETINAFACE_R34_CFG)
    bad_cfg["name"] = "mobilenet0.25"
    with pytest.raises(ValueError, match="resnet34"):
        RetinaFace(bad_cfg)


